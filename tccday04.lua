t=0
sin=math.sin
function TIC()
 t=t+1
 for y=0,135 do
  for x=0,239 do
   c=sin(x/32+t/-128)
    +sin(y/32+t/-128)
    *sin(t/32)
   pix(x,y,c*8+t/12)
  end
 end
end

